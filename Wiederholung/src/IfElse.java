public class IfElse {
    public static void main(String[] args) {
        int zahl = 7;

        if (zahl == 5) {
            System.out.println("5");
        }
        else if (zahl == 6) {
            System.out.println("6");
        }
        else {
            System.out.println("Unbekannt");
        }
    }
}
